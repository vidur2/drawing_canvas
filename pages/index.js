import Head from 'next/head'
import { useEffect, useState } from 'react';
import styles from '../styles/Home.module.css'
import { Matrix, inverse } from "ml-matrix";

export default function Home() {
  const [dragging, setDragging] = useState(false);
  const [loadedImage, setLoadedImage] = useState(false);
  const [loadingPath, setLoadingPath] = useState(false);
  const limit = 100;

  useEffect(() => {
    let background = new Image();
    background.src = "/images/path.jpeg";

    const map = new Array();
    const canvas = document.getElementById("canvas");
    const offsetX = canvas.offsetLeft;
    const offsetY = canvas.offsetTop;
    const ctx = canvas.getContext("2d");

    const downEvent = () => {
      setDragging(true);
      ctx.beginPath()
    };

    canvas.onmousedown = downEvent;
    canvas.ontouchstart = downEvent;

    background.onload = () => {
      if (!loadedImage) {
        canvas.width = background.width;
        canvas.height = background.height;
        ctx.drawImage(background, 0, 0);
        setLoadedImage(true);
      }
    }

    const dragEvent = () => {
      setDragging(false);
      console.log(`(${map[0].t}, ${map[0].x}, ${map[0].y})`)

      genArr(map, "t", "x");
      genArr(map, "t", "y");

      genArr(map, "x", "t");
      genArr(map, "y", "t");
    };

    canvas.onmouseup = dragEvent;
    canvas.ontouchend = dragEvent;

    const moveEvent = (e) => {
      if (dragging) {
        map.push({ t: map.length * .1, x: e.clientX-offsetX-(canvas.clientWidth/2), y: -(e.clientY-offsetY) + (canvas.clientHeight/2) });
        // console.log(canvas.clientWidth);
        // console.log(canvas.clientHeight);
        ctx.lineTo(e.clientX-offsetX, e.clientY-offsetY);
        ctx.stroke();
      }
    }

    canvas.onmousemove = moveEvent;
    canvas.ontouchmove = moveEvent;
  });

  
  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <canvas id="canvas" width="300" height="300"></canvas>
      </main>
    </div>
  )
}


